create table if not exists public.user_account_status (
  user_id uuid primary key references auth.users(id) on delete cascade,
  is_locked boolean not null default false,
  is_active boolean not null default true,
  force_password_reset boolean not null default false,
  locked_reason text,
  deactivated_reason text,
  last_password_reset_at timestamptz,
  temp_password_issued_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  updated_by uuid references auth.users(id) on delete set null
);

create table if not exists public.user_admin_scopes (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  scope_type text not null check (scope_type in ('global', 'program')),
  program_id text,
  created_at timestamptz not null default now(),
  created_by uuid references auth.users(id) on delete set null,
  constraint user_admin_scopes_shape_chk check (
    (scope_type = 'global' and program_id is null)
    or (scope_type = 'program' and program_id is not null)
  )
);

create unique index if not exists user_admin_scopes_global_uniq
  on public.user_admin_scopes (user_id, scope_type)
  where scope_type = 'global';

create unique index if not exists user_admin_scopes_program_uniq
  on public.user_admin_scopes (user_id, program_id)
  where scope_type = 'program';

