-- ------------------------------
-- 005_seed_initial_admin.sql
-- ------------------------------
do $$
declare
  v_admin_email text := 'sanam.vishwakamal@gmail.com';
  v_user_id uuid;
begin
  select id
  into v_user_id
  from auth.users
  where lower(email) = lower(v_admin_email)
  limit 1;

  if v_user_id is null then
    raise exception 'User not found in auth.users for email: %', v_admin_email;
  end if;

  insert into public.user_roles (user_id, role)
  values (v_user_id, 'admin')
  on conflict (user_id) do update set role = excluded.role;

  insert into public.user_account_status (user_id)
  values (v_user_id)
  on conflict (user_id) do nothing;

  delete from public.user_admin_scopes
  where user_id = v_user_id;

  insert into public.user_admin_scopes (user_id, scope_type)
  values (v_user_id, 'global')
  on conflict do nothing;
end $$;

